def get_all_municipalities():
    """Return list of all municipalities with their data."""
    return [
        {
            "code": code,
            "name": data["name"],
            "lat": data["lat"],
            "lng": data["lng"],
            "department_code": data["department_code"]
        }
        for code, data in ALL_MUNICIPALITIES.items()
    ]


def get_municipalities_by_department_code(dept_code: str):
    """Return municipalities for a specific department."""
    if dept_code not in DEPARTMENT_MUNICIPALITIES:
        return []
    
    return [
        {
            "code": code,
            "name": data["name"],
            "lat": data["lat"],
            "lng": data["lng"],
            "department_code": dept_code
        }
        for code, data in DEPARTMENT_MUNICIPALITIES[dept_code].items()
    ]


def find_nearest_municipality(lat: float, lng: float, max_distance_km: float = 100):
    """
    Find the nearest municipality to given coordinates.
    Returns municipality data or None if none found within max_distance.
    
    Uses simple Euclidean distance (appropriate for Colombia's location near equator).
    1 degree ≈ 111 km at equator.
    """
    nearest = None
    min_dist = float('inf')
    
    for code, data in ALL_MUNICIPALITIES.items():
        # Calculate approximate distance in degrees
        dist = ((data["lat"] - lat) ** 2 + (data["lng"] - lng) ** 2) ** 0.5
        
        if dist < min_dist:
            min_dist = dist
            nearest = {
                "code": code,
                "name": data["name"],
                "lat": data["lat"],
                "lng": data["lng"],
                "department_code": data["department_code"],
                "distance_deg": dist
            }
    
    # Convert distance threshold to degrees (approximate)
    max_dist_deg = max_distance_km / 111.0
    
    if nearest and nearest["distance_deg"] <= max_dist_deg:
        return nearest
    return None


def get_municipality_by_code(code: str):
    """Get municipality by DANE code."""
    if code in ALL_MUNICIPALITIES:
        data = ALL_MUNICIPALITIES[code]
        return {
            "code": code,
            "name": data["name"],
            "lat": data["lat"],
            "lng": data["lng"],
            "department_code": data["department_code"]
        }
    return None


def get_municipalities_count():
    """Return total count of municipalities."""
    return len(ALL_MUNICIPALITIES)
